# --- PDF GENERATOR FUNCTION ---
def generate_pdf_report(inputs, pred, prob, features, weights):
    pdf = FPDF()
    pdf.add_page()
    
    # Header
    pdf.set_font("Arial", 'B', 16)
    pdf.cell(0, 10, "Loan Default Risk Assessment Report", ln=True, align='C')
    pdf.ln(10)
    
    # Applicant Profile
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, "Applicant Profile:", ln=True)
    pdf.set_font("Arial", '', 11)
    for key, val in inputs.items():
        pdf.cell(0, 8, f"{key}: {val}", ln=True)
    pdf.ln(5)
    
    # Assessment Result
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, "Assessment Result:", ln=True)
    pdf.set_font("Arial", '', 11)
    status = "HIGH RISK (DEFAULT PREDICTED)" if pred == 1 else "APPROVED (LOW RISK)"
    pdf.cell(0, 8, f"Status: {status}", ln=True)
    pdf.cell(0, 8, f"Probability of Default: {prob:.2%}", ln=True)
    pdf.ln(5)
    
    # Explanation / Feature Importance
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, "Model Explanation (Feature Impact):", ln=True)
    pdf.set_font("Arial", '', 11)
    pdf.multi_cell(0, 8, "The following shows how much each factor influenced the decision. Positive weights increase the risk of default, while negative weights decrease the risk.")
    
    impacts = sorted(zip(features, weights), key=lambda x: x[1], reverse=True)
    for feat, weight in impacts:
        impact_dir = "Increases Risk" if weight > 0 else "Decreases Risk"
        pdf.cell(0, 8, f"- {feat}: {weight:.4f} ({impact_dir})", ln=True)
        
    pdf.ln(15)
    pdf.set_font("Arial", 'I', 9)
    pdf.cell(0, 10, "Generated by Ca' Foscari Loan Analytics System.", ln=True)
    
    # Save to a temporary file to safely return bytes
    with tempfile.NamedTemporaryFile(delete=False, suffix=".pdf") as tmp:
        pdf.output(tmp.name)
        with open(tmp.name, "rb") as f:
            pdf_bytes = f.read()
    os.remove(tmp.name) # Clean up temp file
    
    return pdf_bytes

