st.title("💰 Loan Default Risk Portal")

if scaler and model:
    tab1, tab2, tab3 = st.tabs(["Individual", "Batch", "Insights"])

    # --- TAB 1: INDIVIDUAL PREDICTION ---
    with tab1:
        st.markdown('<div id="single-assessment"></div>', unsafe_allow_html=True)
        col_in, col_res = st.columns([1.2, 1])
        
        with col_in:
            with st.container(border=True):
                st.subheader("Applicant Data")
                # Wrap inputs in a form so it doesn't predict on every keystroke
                with st.form("input_form"):
                    age = st.number_input("Age", 18, 100, 35)
                    income = st.number_input("Annual Income ($)", 0, 1000000, 50000)
                    credit = st.slider("Credit Score", 300, 850, 650)
                    dti = st.slider("DTI Ratio", 0.0, 1.0, 0.3)
                    
                    c1, c2 = st.columns(2)
                    job_yrs = c1.number_input("Years at Job", 0, 50, 5)
                    loans = c2.number_input("Existing Loans", 0, 20, 1)
                    
                    analyze_btn = st.form_submit_button("Run Risk Assessment", type="primary")

        # Execute Prediction and save to session state
        if analyze_btn:
            input_data = pd.DataFrame([[age, income, credit, job_yrs, loans, dti]], 
                                     columns=['Age', 'Annual_Income', 'Credit_Score', 'Years_at_Job', 'Existing_Loans', 'DTI_Ratio'])
            scaled = scaler.transform(input_data)
            
            # Save results to session state
            st.session_state['prob'] = model.predict_proba(scaled)[0][1]
            st.session_state['pred'] = model.predict(scaled)[0]
            st.session_state['inputs'] = {
                "Age": age, "Annual Income ($)": income, "Credit Score": credit, 
                "Years at Job": job_yrs, "Existing Loans": loans, "DTI Ratio": dti
            }
            st.session_state['assessed'] = True

        # Display results ONLY if an assessment has been run and saved in state
        with col_res:
            if st.session_state['assessed']:
                st.subheader("Risk Assessment")
                pred = st.session_state['pred']
                prob = st.session_state['prob']
                
                st.markdown(f"""
                    <div class="result-card">
                        <h3 style="margin-top:0; color:#444;">Analysis Summary</h3>
                        <p style="color:#666;">Status: <b style="color:{'#e74c3c' if pred==1 else '#27ae60'}">
                        {'DEFAULT RISK' if pred==1 else 'APPROVED'}</b></p>
                    </div>
                    """, unsafe_allow_html=True)
                
                st.metric(label="Probability of Default", value=f"{prob:.2%}")
                st.progress(prob)
                
                # Generate PDF Bytes
                pdf_data = generate_pdf_report(
                    inputs=st.session_state['inputs'], 
                    pred=pred, 
                    prob=prob, 
                    features=scaler.feature_names_in_, 
                    weights=model.coef_[0]
                )
                
                # Download Button (does not clear the screen now due to session state!)
                st.download_button(
                    label="📄 Download Assessment Report (PDF)",
                    data=pdf_data,
                    file_name="Risk_Assessment_Report.pdf",
                    mime="application/pdf",
                    type="primary"
                )
            else:
                st.info("👈 Enter applicant data and click 'Run Risk Assessment' to see results here.")

    # --- TAB 2: BATCH PROCESSING ---
    with tab2:
        st.markdown('<div id="batch-upload"></div>', unsafe_allow_html=True)
        st.subheader("📂 Batch Processing")
        uploaded_file = st.file_uploader("Upload CSV Data", type="csv")
        if uploaded_file:
            df = pd.read_csv(uploaded_file)
            required = ['Age', 'Annual_Income', 'Credit_Score', 'Years_at_Job', 'Existing_Loans', 'DTI_Ratio']
            if all(col in df.columns for col in required):
                batch_scaled = scaler.transform(df[required])
                df['Risk Score'] = model.predict_proba(batch_scaled)[:, 1]
                df['Decision'] = np.where(model.predict(batch_scaled) == 1, 'Deny', 'Approve')
                st.dataframe(df, use_container_width=True)
                csv = df.to_csv(index=False).encode('utf-8')
                st.download_button("Download Batch Results", csv, "batch_results.csv", "text/csv")
            else:
                st.error(f"Missing required columns. Please include: {required}")

    # --- TAB 3: INSIGHTS ---
